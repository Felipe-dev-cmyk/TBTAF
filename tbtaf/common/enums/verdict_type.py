class TBTAFVerdictType(object):
    '''
    Simple enumeration class that describes the verdict of a given TBTAFTestCase execution
    '''
    #Verdict enumeration types
    
    #The verdict for a passing test
    PASS="Passed"
    #The verdict for a failed test
    FAIL="Failed"
    #The verdict for an inconclusive test
    INCONCLUSIVE="Inconclusive"


        