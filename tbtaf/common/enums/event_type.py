class TBTAFEventType(object):
    '''
    Simple enumeration to describe the type of a given event within the TBTAF
    '''
    
    #Event enumeration types
    
    #This event entry is just for information purposes
    INFO="INFORMATION"
    #This event entry contains information related to a given warning
    WARNING="WARNING"
    #This event entry contains information related to a given error
    ERROR="ERROR"

        