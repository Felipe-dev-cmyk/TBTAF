class TBTAFTestSuiteType(object):
    '''
    Simple enumeration class that defines the types of Test suites supported by the TBTAF
    '''
    #Test suite enumeration types
    
    #This enumeration corresponds to the classic test suite, targeted for brute force approach execution
    NORMAL="Normal Suite"
    #This enumeration corresponds to the smart test suite, which is tag-enabled for smart automated execution
    SMART="Smart Suite"

        